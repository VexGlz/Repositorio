    private void cargarClientes() {
        tblClientes.setModel(clController.obtenerTablaClientesPorFiltroModal(""));
    }

    private void buscar() {
        String nombre = txtBuscar.getText().trim();
        if (nombre.isEmpty()) {
            cargarClientes();
        } else {
            tblClientes.setModel(clController.obtenerTablaClientesPorFiltroModal(nombre));
        }
    }

    public Integer getClienteSeleccionado() {
        int fila = tblClientes.getSelectedRow();
        if (fila >= 0) {
            return (Integer) tblClientes.getValueAt(fila, 0); // columna 0 = id
        }
        return null;
    }
